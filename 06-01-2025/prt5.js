// array - properties and methods
/**
 * push() - add one /more elements to the end
 * unshift() - add one /more elements to the start
 * pop() - remove the last element
 * shift() - remove the first element
 */

var cart = ["A","B","C","D"];
console.log(cart);
cart.push("E");

console.log(cart);

cart.unshift("0");
console.log(cart);

cart.pop();
console.log(cart);

cart.shift();
console.log(cart);